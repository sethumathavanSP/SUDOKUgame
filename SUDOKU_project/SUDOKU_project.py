# defining a code to convert string of list of number to list of numbers

def list_of(a):
    p = list(a)
    p.pop(0)
    p.pop(len(p)-1)
    last = ''

    for i in p:
        if i != ',':
            last += i
        else:
            first = last
            last = ''
    return [int(first),int(last)]
        
